#include "../../inc/ft_printf.h"

int	ft_printstr(char *str)
{
	int	i;

	i = 0;
	if (!str)
		str = "(null)";
	while (*str)
	{
		ft_printchar((int)*str);
		i++;
		str++;
	}
	return (i);
}
